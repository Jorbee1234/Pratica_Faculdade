public class somar {

	public int mat(Integer num1, Integer num2) {
		return num1+num2;
	}
	public Double div(Integer num1, Integer num2) {
		return Double.valueOf(num1)/ Double.valueOf(num2);
	}
    public int multi(Integer num1, Integer num2) {
    	return num1*num2;
    }
    public int sub(Integer num1, Integer num2) {
    	return num1-num2;
    }
}
